def rgb_to_bn(i):
    """
    Transforma una imatge RGB en una imatge 1 utilitzant la luminancia dels pixels,
    l'histograma de la imatge i el llindar d'aquest
    >>> rgb_to_bn(('RGB', [[(255, 255, 255), (255, 255, 255), (255, 255, 255)],[(255, 255, 255), (255, 255, 255), (255, 255, 255)], [(255, 255, 255), (255, 255, 255), (255, 255, 255)]]))
    ('1', [[255, 255, 255], [255, 255, 255], [255, 255, 255]])
    >>> rgb_to_bn(('RGB', [[(0, 0, 100), (0, 100, 0), (100, 0, 0)],[(255, 255, 255), (255, 255, 255), (255, 255, 255)], [(255, 255, 255), (255, 255, 255), (255, 255, 255)]]))
    ('1', [[0, 0, 0], [255, 255, 255], [255, 255, 255]])
    """
    #Discriminem entre imatges RGB i en escala de grisos
    if format(i)=='RGB':
        img=luminance_img(i)
    else:
        img=i
    #Obtenim l'histograma de la imatge i a partir d'aquest el llindar
    matriupixels=matrix(img)
    h=histogram(img)
    llindar=get_threshold(h)
    #Per a cada pixel en escala de grisos, li atribuim blanc o negre segons el llindar
    novamatriupixels=[]
    for fila in matriupixels:
        novafila=[]
        for pixel in fila:
            if pixel<=llindar:
                novafila+=[0]
            else:
                novafila+=[255]
        novamatriupixels+=[novafila]
    return ('1', novamatriupixels)
